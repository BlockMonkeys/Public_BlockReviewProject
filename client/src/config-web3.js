// InnerWallet GSN;
export const sendGSN_tx = async(func, pubKey, privateKey=null) => {
    const data = func.encodeABI();
    const to = reviewContract.address;
    const from = pubKey;
    const estimatedGas = await web3.eth.estimateGas({
        data,
        to,
        from
    })

    if(privateKey !== null){
        await gsnProvider.addAccount(privateKey);
    }
    const result = await func.send({ from : pubKey, gasPrice: "20000000000", gas: estimatedGas });
    console.log(`----Gas Station Network----`)
    console.log(result);
    return result;
};

